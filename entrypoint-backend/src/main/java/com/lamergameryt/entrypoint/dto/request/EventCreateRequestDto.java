package com.lamergameryt.entrypoint.dto.request;

import com.fasterxml.jackson.annotation.JsonFormat;
import com.fasterxml.jackson.annotation.JsonProperty;
import io.swagger.v3.oas.annotations.media.Schema;
import jakarta.validation.constraints.Future;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.NotNull;
import jakarta.validation.constraints.Size;
import java.time.LocalDateTime;

public record EventCreateRequestDto(
        @Size(max = 255) @NotBlank String name,
        String description,

        @Schema(
                description =
                        "The start date for the event in yyyy-MM-dd HH:mm:ss. See the example for more information.",
                example = "2025-10-25 12:00:00")
        @JsonProperty("start_date")
        @JsonFormat(pattern = "yyyy-MM-dd HH:mm:ss")
        @Future @NotNull LocalDateTime startDate) {}
